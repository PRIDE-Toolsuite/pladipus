    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        try {
            //check if old password is correct
            String oldPass = new String(pfOldPass.getPassword());
            if (uService.verifyUser(user, oldPass)) {
                String newPass = new String(pfNewPass.getPassword());
                String repeatPass = new String(pfRepeatPass.getPassword());
                if (newPass.equals(repeatPass)) {
                    uService.updatePassword(user, newPass);
                    JOptionPane.showMessageDialog(this,
                            "Password was updated",
                            "Password updated",
                            JOptionPane.OK_OPTION);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this,
                            "The new passwords do not match",
                            "Inane warning",
                            JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "Please input your current password correctly",
                        "Inane warning",
                        JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException | UnsupportedEncodingException ex) {
            JOptionPane.showMessageDialog(this,
                    ex,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnConfirmActionPerformed

